/*
 * The switch Statement
 */

function title() {
    return "The switch Statement";
}

function run() {
    switch(0) {
    case 1:
    case 2:
        print('a');
        break;
    case 3:
        print('b');
        break;
    default:
        print('c');
        break;
    }
}

