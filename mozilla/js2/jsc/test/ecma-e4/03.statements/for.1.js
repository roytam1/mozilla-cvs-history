/*
 * The for Statement
 */

function title() {
    return "The for Statement";
}

function run() {
    for(;;) print('a') 
    for(;i<100;) print('a') 
    for(;i<100;i++) print('a') 
    for(var i:int=0;i<100;i++) print('a') 
}

